package day03_comments_escape_sequence;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("I love \"java\" programming");//Here we use\" to put quotations around java

        System.out.println(" I like to cook\\eat");//this puts a single quote around cook
        System.out.println("I like \'cook\'\\eat"); // the \n make the letters go to next line
        System.out.println("");

        System.out.println("Tomorrow is Friday. \n It is a day off");// \t will give a
        System.out.println("\t\tMessage");

    }
}
