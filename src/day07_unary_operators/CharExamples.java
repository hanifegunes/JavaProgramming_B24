package day07_unary_operators;

public class CharExamples {
    public static void main(String[] args) {
        char letter = 'A';
        System.out.println("letter " + letter);
        System.out.println("letter " + ++letter);
        System.out.println("letter " + letter++);
        System.out.println("letter " + letter++);
        System.out.println("letter " + letter++);
        System.out.println("letter " + letter++);
        System.out.println("letter " + letter++);



    }
}
