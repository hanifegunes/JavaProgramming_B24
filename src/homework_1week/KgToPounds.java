package homework_1week;

public class KgToPounds {
    public static void main(String[] args) {
        int kg = 10;
        double pound = 0.454;// kg
        double totalKg = kg/pound;
        System.out.println(kg+" kg equal to "+totalKg +"pounds");
        int kg1 = 20;
        double totalKg2 = kg1/pound;

        System.out.println(kg1+ " kg equal to "+ totalKg2+ " pounds");

    }
}
